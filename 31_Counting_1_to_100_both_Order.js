document.write("<h2>Print 1 to 100 Ascending Order</h2>");

var a = 1;
while(a <= 100){
    document.write(a, "<br>");
    a++;
}

document.write("<br>");

document.write("<h2>Print 100 to 1 Descending Order</h2>");
var b = 100;
while(b >= 1){
    document.write(b, "<br>");
    b--;
}

document.write("<br>");
