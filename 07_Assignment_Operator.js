// = 
var a = 5;
document.write(a);
document.write("<br>");

// +=
var a = 10;
var b = 5;
a += b;     // a = a + b
document.write(a);
document.write("<br>");

// -=
var a = 15;
var b = 5;
a -= b;     // a = a - b
document.write(a);
document.write("<br>");

// *=
var a = 8;
var b = 15;
a *= b;     // a = a * b
document.write(a);
document.write("<br>");

// /=
var a = 50;
var b = 10;
a /= b;     // a = a / b
document.write(a);
document.write("<br>");

// %=
var a = 10;
var b = 5;
a %= b;     // a = a % b
document.write(a);
document.write("<br>");

// **=
var a = 2;
var b = 5;
a **= b;       // a = a ** b
document.write(a);
document.write("<br>");

